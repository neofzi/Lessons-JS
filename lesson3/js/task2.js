// З клавіатури вводиться ціна товару і кількість грошей. Якщо грошей не вистачає то відмовляємо у покупці, інакше, якщо ще залишаються гроші, то пропонуємо купити лотерею за 4 грн.
let productPrice = parseFloat(prompt("Введіть ціну товару"));
let quantityOfMoney = parseFloat(prompt("Введіть суму ваших грошей"));

if (productPrice > quantityOfMoney) {
  alert(`Вибачте, у вас недостатньо коштів`);
} else {
  confirm("Чи хочете ви придбати лотерею за 4грн?");
}
