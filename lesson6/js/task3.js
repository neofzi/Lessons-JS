// З клавіатури вводиться початкове знчення суми. Поки сума є меншою за 100 додавати до неї числа, які вводяться з клавіатури.
let userNumber;
let sum = 0;
do {
  userNumber = parseFloat(
    prompt(
      "Введіть початкове значення, числа будуть додаватись поки їх сума не буде дорівнювати 100"
    )
  );
  if (sum < 100) {
    sum += userNumber;
    alert(`Сума = ${sum}`);
  }
} while (sum < 100);
