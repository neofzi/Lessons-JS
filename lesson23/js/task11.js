// Визначити чи може бути рядок тексту номером банківської картки (приклад: «4142-3433-2323-3434»). Знайти усі такі номери.
let cartNumber = "4142-3433-2323-3434";
let exp = /\b\d{4}-\d{4}-\d{4}-\d{4}\b/;
let matches = cartNumber.match(exp);
if (matches) alert("Знайдені номери карток: " + matches.join(", "));
else alert("Немає номерів карток у тексті");
